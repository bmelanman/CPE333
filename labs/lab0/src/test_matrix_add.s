################################################################################
# MatrixAdd: Adds two matrices of length `N` together.                         #
################################################################################
# Inputs: a0 - The number of elements `N` in the given list.                   #
#         a1 - The address of the first matrix.                                #
#         a2 - The address of the second matrix.                               #
#         a3 - The address of the matrix to store the result in.               #
# Modifies: t0, t1, t2, t3, t4, t5                                             #
################################################################################
matrix_add:
    beq         a0, zero, matrix_add_ret

    # Save registers t0 - t5
    addi        sp, sp, -24
    sw          t0, 0(sp)
    sw          t1, 4(sp)
    sw          t2, 8(sp)
    sw          t3, 12(sp)
    sw          t4, 16(sp)
    sw          t5, 20(sp)

    mv          t0, zero    # i = 0
    mv          t1, a1      # a1 = matA
    mv          t2, a2      # a2 = matB
    mv          t3, a3      # a3 = matC

matrix_add_loop:
    lw          t4, 0(t1)   # t4 = matA[i]
    lw          t5, 0(t2)   # t5 = matB[i]

    add         t5, t5, t4  # t5 = matA[i] + matB[i]
    sw          t5, 0(t3)   # matC[i] = matA[i] + matB[i]

    addi        t1, t1, 4   # matA++
    addi        t2, t2, 4   # matB++
    addi        t3, t3, 4   # matC++

    addi        t0, t0, 1   # if ( ++i < N ); then loop!
    blt         t0, a0, matrix_add_loop

matrix_add_ret:
    # Restore registers t0 - t5
    lw          t0, 0(sp)
    lw          t1, 4(sp)
    lw          t2, 8(sp)
    lw          t3, 12(sp)
    lw          t4, 16(sp)
    lw          t5, 20(sp)
    addi        sp, sp, 24

    ret
